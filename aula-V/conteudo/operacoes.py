class OperacoesMatematicas:
    def soma(self, numero_1, numero_2):
        return numero_1 + numero_2

    def subtracao(self, numero_1, numero_2):
        return numero_1 - numero_2

    def divisao(self, numero_1, numero_2):
        return numero_1 / numero_2

    def multiplicacao(self, numero_1, numero_2):
        return numero_1 * numero_2


